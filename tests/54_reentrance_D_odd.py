    def test_pattern_first_half_zero_second_half_one(self, grp):
        """Sub-threshold: first (n-1)/2 levels empty, last (n-1)/2 filled.

        Observed for all tested n. With binary occupation and duality,
        this follows if m(χ₂, j) is monotone non-decreasing — verified
        empirically for D₃ through D₁₁.
        """
        n = grp['n']
        j_star = grp['j_star']
        # Half-integers: 1/2, 3/2, ..., j*-1/2. Count = j* = n-1.
        mults = [m_chi(two_j / 2.0, grp, 'chi2')
                 for two_j in range(1, 2 * j_star, 2)]
        # First half should be 0, second half should be 1
        mid = len(mults) // 2
        assert all(m == 0 for m in mults[:mid]), (
            f"D_{grp['n']}: first half not all zero: {mults[:mid]}")
        assert all(m == 1 for m in mults[mid:]), (
            f"D_{grp['n']}: second half not all one: {mults[mid:]}")


