class TestTensorialDualityFails:
    """Tensorial characters: duality sums are 0 or 2, never 1."""

    @pytest.fixture(params=[3, 5, 7, 9, 11], scope='class')
    def grp(self, request):
        return prepare_group(request.param)

    def test_sum_is_0_or_2_chi0(self, grp):
        j_star = grp['j_star']
        for j in range(j_star + 1):
            jd = j_star - j
            if jd < j:
                break
            s = m_chi(j, grp, 'chi0') + m_chi(jd, grp, 'chi0')
            assert s in (0, 2), f"D_{grp['n']}: m(chi0,{j})+m(chi0,{jd})={s}"

    def test_sum_is_0_or_2_chi1(self, grp):
        j_star = grp['j_star']
        for j in range(j_star + 1):
            jd = j_star - j
            if jd < j:
                break
            s = m_chi(j, grp, 'chi1') + m_chi(jd, grp, 'chi1')
            assert s in (0, 2), f"D_{grp['n']}: m(chi1,{j})+m(chi1,{jd})={s}"

    def test_converse_formula_chi0(self, grp):
        """m(χ₀, 0) + m(χ₀, j*) = 2 (both endpoints filled)."""
        m0 = m_chi(0, grp, 'chi0')
        mj = m_chi(grp['j_star'], grp, 'chi0')
        assert m0 == 1 and mj == 1
        assert m0 + mj == 2

    def test_chi0_chi1_complementary(self, grp):
        """At each sub-threshold j, exactly one of χ₀, χ₁ is filled.

        Proof: m(χ₀)+m(χ₁) = #{weights m ∈ [-j,j] with n|m} for the
        rotation subgroup C_n. For j ≤ n-1, only m=0 qualifies, giving
        total = 1. The reflection acts by ±1 on this 1D invariant space,
        assigning it to χ₀ or χ₁ exclusively.
        """
        j_star = grp['j_star']
        for j in range(j_star + 1):
            m0 = m_chi(j, grp, 'chi0')
            m1 = m_chi(j, grp, 'chi1')
            assert m0 + m1 == 1, f"D_{grp['n']} j={j}: chi0+chi1={m0+m1}"


