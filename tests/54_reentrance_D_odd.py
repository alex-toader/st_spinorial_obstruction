class TestD2ModeVanishing:
    """The d=2 mode vanishes at all half-integer j (the mechanism)."""

    def test_chi_j_at_trace_zero(self):
        """χ_j(trace=0) = 0 for all half-integer j."""
        for two_j in range(1, 40, 2):
            j = two_j / 2.0
            val = chi_su2(j, 0.0)
            assert abs(val) < 1e-10, f"j={j}: chi_j(trace=0) = {val}"

    @pytest.fixture(params=[3, 5, 7, 9, 11], scope='class')
    def grp(self, request):
        return prepare_group(request.param)

    def test_only_d2_fails_condition_B(self, grp):
        """In D_n (n odd), only order-2 SO(3) elements fail condition (B)."""
        elements = grp['elements']
        H = grp['H']
        seen = set()
        for q in elements:
            k = qkey(q)
            neg_k = qkey(-q)
            if neg_k in seen:
                continue
            seen.add(k)
            # abs(q[0]) folds half-angle to [0, π/2]: q and -q give the
            # same SO(3) rotation, so the SO(3) order is determined by
            # the folded half-angle.
            cos_ha = abs(q[0])
            if cos_ha > 1 - 1e-12:
                continue
            ha = np.arccos(np.clip(cos_ha, -1, 1))
            for d in range(1, 200):
                if abs(d * ha / np.pi - round(d * ha / np.pi)) < 1e-8:
                    break
            assert H % d == 0, f"d={d} does not divide |H|={H}"
            ratio = H // d
            if ratio % 2 == 1:
                assert d == 2, (
                    f"D_{grp['n']}: element with SO(3) order {d} fails (B), "
                    f"|H|/d = {ratio}")


