def conj_classes(elements):
    remaining = list(range(len(elements)))
    classes = []
    while remaining:
        rep_idx = remaining[0]
        rep = elements[rep_idx]
        cls, new_rem = [], []
        for idx in remaining:
            g = elements[idx]
            is_conj = any(np.allclose(qmul(qmul(h, g), qinv(h)), rep, atol=1e-10)
                          for h in elements)
            (cls if is_conj else new_rem).append(idx)
        classes.append(cls)
        remaining = new_rem
    return classes


