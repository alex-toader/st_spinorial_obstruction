def chi_j(j, trace):
    """SU(2) spin-j character at element with given SU(2) trace."""
    if abs(trace - 2) < 1e-10:
        return float(int(2 * j + 1))
    if abs(trace + 2) < 1e-10:
        return float(int(2 * j + 1) * ((-1) ** (int(round(2 * j)))))
    ha = np.arccos(np.clip(trace / 2, -1, 1))
    d = np.sin(ha)
    if abs(d) < 1e-14:
        return float(int(2 * j + 1))
    return np.sin((2 * j + 1) * ha) / d


